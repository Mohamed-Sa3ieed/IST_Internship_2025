def check_valid_id(id: str):
    if not id.startswith(("isbn-", "imdb-")):
        raise ValueError('Invalid ID format, it must start with "isbn-" or "imdb-"')
    return id


@app.get("/items/")
async def read_items(
    id: Annotated[str | None, AfterValidator(check_valid_id),Query()] = None,
):
    if id:
        item = data.get(id)
    else:
        _id, item = random.choice(list(data.items()))
    return {"id": _id, "name": item}

